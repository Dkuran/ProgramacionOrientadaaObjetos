    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:
        if(id < 4){
            ClsFruta frcomp = new ClsFruta();
            frcomp = frutas[seleccionada];
            //frutas[0].setNombre("Frutilla"); no hacer esto
            frcomp.setCantidad(Integer.parseInt(txtcantidad.getText()));
            frcomp.precioTotal();
            frutasComp[id] =frcomp;
            id++;
        }else{
            JOptionPane.showMessageDialog(rootPane, "Solo puede comprar 4 frutas");
        }
        //txtareaimpresion.setText(frcomp.imprimir());
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void btnfacturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfacturarActionPerformed
        // TODO add your handling code here:
        String factura = encabezado +"\n"+ linea+"\n";
        double total = 0;
        
        for(int i = 0; i < id ; i++){
            factura = factura + frutasComp[i].imprimir()+"\n";
            total = total + frutasComp[i].getTotal();
        }
        factura = factura +"\n"+ linea+ "\nTotal a Pagar: $"+ String.valueOf(total);
        txtareaimpresion.setText(factura);
        for(int i = 0; i < id ; i++){
            frutasComp[i] = null;
        }
        id = 0;
        
    }//GEN-LAST:event_btnfacturarActionPerformed

