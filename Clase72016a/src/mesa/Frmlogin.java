    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:

        ClsUsuario nuevo = new ClsUsuario(id, txtcedula.getText(), txtnombre.getText(), txtapellido.getText());
        usuarios[id] = nuevo;
        id++;
        JOptionPane.showMessageDialog(rootPane, "Usuario Creado\n"+
                nuevo.getNombre()+" "+nuevo.getApellido()+"\n"+
                "Su usuario es: "+nuevo.getUsr());
        limpiar();
        txtusr.setText(nuevo.getUsr());
    }//GEN-LAST:event_btnregistrarActionPerformed

