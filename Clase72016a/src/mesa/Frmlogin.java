    private ClsUsuario revisarPass(String pass, String usr){
        ClsUsuario actual = null; 
        for(int i = 0 ; i < id; i++){
            //System.out.println(pass);
            //System.out.println(usuarios[i].getCedula());
            if(usuarios[i].pass(pass, usr) == true ){
                actual = usuarios[i];
            }
        }
        return actual;
    }
    
    private void limpiar(){
        jpfpass.setText("");
        txtusr.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtcedula.setText("");
                
    }
    
    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        // TODO add your handling code here:
        //System.out.println(jpfpass.getText());
        

        ClsUsuario nuevo = revisarPass(jpfpass.getText(), txtusr.getText());
        if(nuevo == null){
            JOptionPane.showMessageDialog(rootPane, "Usuario no registrado");
            limpiar();
        }else{
            FrmUsuarios user = new FrmUsuarios(nuevo);
            user.setVisible(true);
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_btnloginActionPerformed

